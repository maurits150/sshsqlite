package org.sshsqlite.jdbc;

import com.fasterxml.jackson.databind.JsonNode;

import java.sql.*;

public final class SshSqliteStatement extends BaseStatement {
    private final SshSqliteConnection connection;
    private ResultSet currentResultSet;
    private ResultSet generatedKeys;
    private long updateCount = -1;

    public SshSqliteStatement(SshSqliteConnection connection) {
        super(connection);
        this.connection = connection;
    }

    @Override
    public ResultSet executeQuery(String sql) throws SQLException {
        checkOpen();
        clearExecutionState(true);
        ResultSet resultSet = startQuery(sql);
        currentResultSet = resultSet;
        return resultSet;
    }

    @Override
    public boolean execute(String sql) throws SQLException {
        checkOpen();
        clearExecutionState(true);
        clearGeneratedKeys();
        if (rowProducingStatementCount(sql) > 0) {
            currentResultSet = startQuery(sql);
            return true;
        }
        updateCount = executeUpdateInternal(sql, false);
        return false;
    }

    @Override
    public boolean execute(String sql, int autoGeneratedKeys) throws SQLException {
        checkOpen();
        boolean returnKeys = returnGeneratedKeys(autoGeneratedKeys);
        clearExecutionState(true);
        clearGeneratedKeys();
        if (rowProducingStatementCount(sql) > 0) {
            currentResultSet = startQuery(sql);
            return true;
        }
        updateCount = executeUpdateInternal(sql, returnKeys);
        return false;
    }

    @Override public boolean execute(String sql, int[] columnIndexes) throws SQLException { checkOpen(); throw JdbcSupport.unsupported(); }
    @Override public boolean execute(String sql, String[] columnNames) throws SQLException { checkOpen(); throw JdbcSupport.unsupported(); }

    @Override
    public int executeUpdate(String sql) throws SQLException {
        long count = executeLargeUpdate(sql);
        return count > Integer.MAX_VALUE ? Integer.MAX_VALUE : (int) count;
    }

    @Override
    public long executeLargeUpdate(String sql) throws SQLException {
        checkOpen();
        clearExecutionState(true);
        clearGeneratedKeys();
        updateCount = executeUpdateInternal(sql, false);
        return updateCount;
    }

    @Override
    public int executeUpdate(String sql, int autoGeneratedKeys) throws SQLException {
        long count = executeLargeUpdate(sql, autoGeneratedKeys);
        return count > Integer.MAX_VALUE ? Integer.MAX_VALUE : (int) count;
    }

    @Override public int executeUpdate(String sql, int[] columnIndexes) throws SQLException { checkOpen(); throw JdbcSupport.unsupported(); }
    @Override public int executeUpdate(String sql, String[] columnNames) throws SQLException { checkOpen(); throw JdbcSupport.unsupported(); }

    @Override
    public long executeLargeUpdate(String sql, int autoGeneratedKeys) throws SQLException {
        checkOpen();
        clearExecutionState(true);
        clearGeneratedKeys();
        updateCount = executeUpdateInternal(sql, returnGeneratedKeys(autoGeneratedKeys));
        return updateCount;
    }

    @Override public long executeLargeUpdate(String sql, int[] columnIndexes) throws SQLException { checkOpen(); throw JdbcSupport.unsupported(); }
    @Override public long executeLargeUpdate(String sql, String[] columnNames) throws SQLException { checkOpen(); throw JdbcSupport.unsupported(); }

    @Override
    public ResultSet getGeneratedKeys() throws SQLException {
        checkOpen();
        if (generatedKeys == null) {
            generatedKeys = generatedKeysResultSet(null);
        }
        return generatedKeys;
    }

    @Override
    public ResultSet getResultSet() throws SQLException {
        checkOpen();
        return currentResultSet;
    }

    @Override
    public int getUpdateCount() throws SQLException {
        checkOpen();
        return updateCount > Integer.MAX_VALUE ? Integer.MAX_VALUE : (int) updateCount;
    }

    @Override
    public long getLargeUpdateCount() throws SQLException {
        checkOpen();
        return updateCount;
    }

    @Override
    public boolean getMoreResults() throws SQLException {
        checkOpen();
        closeCurrentResultSet();
        updateCount = -1;
        return false;
    }

    @Override
    public boolean getMoreResults(int current) throws SQLException {
        checkOpen();
        if (current != Statement.KEEP_CURRENT_RESULT) {
            closeCurrentResultSet();
        }
        updateCount = -1;
        return false;
    }

    @Override
    public void close() throws SQLException {
        closeCurrentResultSet();
        clearGeneratedKeys();
        super.close();
    }

    @Override
    public int getResultSetConcurrency() throws SQLException {
        checkOpen();
        return ResultSet.CONCUR_READ_ONLY;
    }

    @Override
    public int getResultSetType() throws SQLException {
        checkOpen();
        return ResultSet.TYPE_FORWARD_ONLY;
    }

    @Override
    public int getResultSetHoldability() throws SQLException {
        checkOpen();
        return ResultSet.CLOSE_CURSORS_AT_COMMIT;
    }

    int effectiveFetchSize() throws SQLException {
        int fetchSize = getFetchSize();
        return fetchSize > 0 ? fetchSize : connection.defaultFetchSize();
    }

    int queryTimeoutMs() throws SQLException {
        int seconds = getQueryTimeout();
        return seconds > 0 ? seconds * 1000 : connection.defaultQueryTimeoutMs();
    }

    SqlClient protocol() throws SQLException {
        return connection.protocol();
    }

    private ResultSet startQuery(String sql) throws SQLException {
        int rowProducingStatements = rowProducingStatementCount(sql);
        if (rowProducingStatements == 0) {
            throw JdbcSupport.invalidArgument("executeQuery requires a statement that returns rows");
        }
        if (rowProducingStatements > 1) {
            throw JdbcSupport.invalidArgument("Multiple row-producing SQL statements are not supported by this JDBC driver");
        }
        closeCurrentResultSet();
        try {
            connection.ensureTransactionForStatement(sql);
            JsonNode response = connection.protocol().query(sql, getMaxRows(), effectiveFetchSize(), queryTimeoutMs());
            connection.syncExplicitTransactionState(sql);
            return new SshSqliteResultSet(this, response.path("cursorId").asText(), response.path("columns"));
        } catch (Exception e) {
            connection.recoverFailedExplicitTransactionScript(sql, e);
            throw connection.sqlException(e);
        }
    }

    private long executeUpdateInternal(String sql, boolean returnGeneratedKeys) throws SQLException {
        connection.ensureWritesAllowed();
        if (rowProducingStatementCount(sql) > 0) {
            throw JdbcSupport.invalidArgument("executeUpdate requires a statement that does not return rows");
        }
        try {
            connection.ensureTransactionForStatement(sql);
            JsonNode response = connection.protocol().exec(sql, null, queryTimeoutMs());
            connection.syncExplicitTransactionState(sql);
            if (returnGeneratedKeys) {
                generatedKeys = generatedKeysResultSet(response.path("lastInsertRowid").asLong());
            }
            return response.path("changes").asLong();
        } catch (Exception e) {
            connection.recoverFailedExplicitTransactionScript(sql, e);
            throw connection.sqlException(e);
        }
    }

    private void clearExecutionState(boolean closeResultSet) throws SQLException {
        if (closeResultSet) {
            closeCurrentResultSet();
        } else {
            currentResultSet = null;
        }
        updateCount = -1;
    }

    private void closeCurrentResultSet() throws SQLException {
        if (currentResultSet != null) {
            currentResultSet.close();
            currentResultSet = null;
        }
    }

    private void clearGeneratedKeys() throws SQLException {
        if (generatedKeys != null) {
            generatedKeys.close();
            generatedKeys = null;
        }
    }

    static boolean returnGeneratedKeys(int autoGeneratedKeys) throws SQLException {
        if (autoGeneratedKeys == Statement.RETURN_GENERATED_KEYS) return true;
        if (autoGeneratedKeys == Statement.NO_GENERATED_KEYS) return false;
        throw JdbcSupport.invalidArgument("autoGeneratedKeys must be RETURN_GENERATED_KEYS or NO_GENERATED_KEYS");
    }

    static ResultSet generatedKeysResultSet(Long lastInsertRowid) {
        java.util.List<BaseResultSetMetaData.Column> columns = java.util.List.of(
                new BaseResultSetMetaData.Column("GENERATED_KEY", "GENERATED_KEY", Types.BIGINT, "INTEGER", Long.class.getName(), false));
        java.util.List<java.util.List<Object>> rows = lastInsertRowid == null ? java.util.List.of() : java.util.List.of(java.util.List.of(lastInsertRowid));
        return new InMemoryResultSet(null, columns, rows);
    }

    static boolean returnsRows(String sql) {
        java.util.List<String> tokens = sqlTokens(sql);
        if (tokens.isEmpty()) {
            return false;
        }
        String first = tokens.get(0).toLowerCase(java.util.Locale.ROOT);
        if (first.equals("pragma") && isNoResultPragmaSetter(sql)) {
            return false;
        }
        if (first.equals("with")) {
            return !containsWriteKeyword(tokens) || containsReturningAfterWriteKeyword(tokens);
        }
        if (first.equals("select") || first.equals("pragma") || first.equals("values") || first.equals("with")) {
            return true;
        }
        return isWriteKeyword(first) && containsReturningAfterWriteKeyword(tokens);
    }

    private static int rowProducingStatementCount(String sql) {
        int count = 0;
        for (String statement : CliProtocolClient.splitSqlStatements(sql)) {
            if (returnsRows(statement)) {
                count++;
            }
        }
        return count;
    }

    private static boolean containsWriteKeyword(java.util.List<String> tokens) {
        for (String token : tokens) {
            if (isWriteKeyword(token.toLowerCase(java.util.Locale.ROOT))) {
                return true;
            }
        }
        return false;
    }

    private static boolean containsReturningAfterWriteKeyword(java.util.List<String> tokens) {
        boolean sawWrite = false;
        for (String token : tokens) {
            String lower = token.toLowerCase(java.util.Locale.ROOT);
            if (isWriteKeyword(lower)) {
                sawWrite = true;
            } else if (sawWrite && lower.equals("returning")) {
                return true;
            }
        }
        return false;
    }

    private static boolean isWriteKeyword(String lower) {
        return lower.equals("insert") || lower.equals("update") || lower.equals("delete") || lower.equals("replace");
    }

    private static boolean isNoResultPragmaSetter(String sql) {
        java.util.List<String> tokens = sqlTokens(sql);
        if (tokens.size() < 2) {
            return false;
        }
        String name = tokens.get(1).toLowerCase(java.util.Locale.ROOT);
        if (name.equals("journal_mode") || name.equals("wal_checkpoint")) return false;
        boolean lineComment = false;
        boolean blockComment = false;
        char quote = 0;
        for (int i = 0; i < sql.length(); i++) {
            char ch = sql.charAt(i);
            if (lineComment) {
                if (ch == '\n' || ch == '\r') lineComment = false;
                continue;
            }
            if (blockComment) {
                if (ch == '/' && i > 0 && sql.charAt(i - 1) == '*') blockComment = false;
                continue;
            }
            if (quote != 0) {
                if ((quote == '[' && ch == ']') || (quote != '[' && ch == quote)) {
                    if (quote == '\'' && i + 1 < sql.length() && sql.charAt(i + 1) == '\'') i++; else quote = 0;
                }
                continue;
            }
            if (ch == '-' && i + 1 < sql.length() && sql.charAt(i + 1) == '-') { lineComment = true; i++; continue; }
            if (ch == '/' && i + 1 < sql.length() && sql.charAt(i + 1) == '*') { blockComment = true; i++; continue; }
            if (ch == '\'' || ch == '"' || ch == '`' || ch == '[') { quote = ch; continue; }
            if (ch == '=') return true;
        }
        return false;
    }

    private static java.util.List<String> sqlTokens(String sql) {
        java.util.ArrayList<String> tokens = new java.util.ArrayList<>();
        StringBuilder current = new StringBuilder();
        boolean lineComment = false;
        boolean blockComment = false;
        char quote = 0;
        for (int i = 0; i < sql.length(); i++) {
            char ch = sql.charAt(i);
            if (lineComment) {
                if (ch == '\n' || ch == '\r') lineComment = false;
                continue;
            }
            if (blockComment) {
                if (ch == '/' && i > 0 && sql.charAt(i - 1) == '*') blockComment = false;
                continue;
            }
            if (quote != 0) {
                if ((quote == '[' && ch == ']') || (quote != '[' && ch == quote)) {
                    if (quote == '\'' && i + 1 < sql.length() && sql.charAt(i + 1) == '\'') {
                        i++;
                        continue;
                    }
                    quote = 0;
                }
                continue;
            }
            if (ch == '-' && i + 1 < sql.length() && sql.charAt(i + 1) == '-') {
                flushToken(tokens, current);
                lineComment = true;
                continue;
            }
            if (ch == '/' && i + 1 < sql.length() && sql.charAt(i + 1) == '*') {
                flushToken(tokens, current);
                blockComment = true;
                continue;
            }
            if (ch == '\'' || ch == '"' || ch == '`' || ch == '[') {
                flushToken(tokens, current);
                quote = ch;
                continue;
            }
            if (Character.isLetter(ch) || ch == '_') {
                current.append(ch);
            } else {
                flushToken(tokens, current);
            }
        }
        flushToken(tokens, current);
        return tokens;
    }

    private static void flushToken(java.util.List<String> tokens, StringBuilder current) {
        if (current.length() > 0) {
            tokens.add(current.toString());
            current.setLength(0);
        }
    }
}
